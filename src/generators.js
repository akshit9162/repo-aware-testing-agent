async function changedFiles() {
  if (!changedMode) return null;
  const base = process.env.QA_CHANGED_BASE || 'origin/main';
  return new Promise((resolve) => {
    const child = spawn('git', ['diff', '--name-only', base + '...HEAD']);
    let out = '';
    child.stdout.on('data', (d) => { out += d.toString(); });
    child.on('close', () => resolve(out.split('\\n').map((s) => s.trim()).filter(Boolean)));
    child.on('error', () => resolve([]));
  });
}

function touchedBy(script, changed) {
  const profile = STAGE_PROFILE[script];
  if (!profile?.touches?.length) return true;
  return changed.some((file) => profile.touches.some((prefix) => file.startsWith(prefix)));
}

function runScript(script, { timeoutMs = stageTimeoutMs } = {}) {
  return new Promise((resolve) => {
    const started = Date.now();
    process.stdout.write('\\n[qa:all] running ' + script + ' (' + (STAGE_PROFILE[script]?.tier || 'unknown') + ')\\n');
    const child = spawn('npm', ['run', script], {
      stdio: 'inherit',
      shell: process.platform === 'win32',
    });
    const timer = setTimeout(() => { try { child.kill('SIGKILL'); } catch {} }, timeoutMs);
    child.on('error', (error) => {
      clearTimeout(timer);
      record(script, 'error', { exitCode: -1, durationMs: Date.now() - started, reason: error.message });
      resolve(false);
    });
    child.on('close', (code) => {
      clearTimeout(timer);
      record(script, code === 0 ? 'passed' : 'failed', { exitCode: code, durationMs: Date.now() - started });
      resolve(code === 0);
    });
  });
}

async function waitForHealth(url, deadlineMs) {
  const deadline = Date.now() + deadlineMs;
  while (Date.now() < deadline) {
    try {
      const response = await fetch(url, { method: 'GET' });
      if (response.status < 500) return true;
    } catch {}
    await new Promise((r) => setTimeout(r, 1000));
  }
  return false;
}

function pickAppServerScript() {
  const override = process.env.QA_APP_SERVER;
  if (override && pkg.scripts?.[override]) return override;
  // Prefer 'dev' by default — it's a single command that doesn't require a prior build
  // step, which makes the orchestrator usable on a fresh clone. Set QA_APP_SERVER=start
  // (and ensure a production build exists) when you want production-mode behavior, e.g.
  // for representative perf runs.
  if (pkg.scripts?.dev) return 'dev';
  if (pkg.scripts?.start) return 'start';
  return null;
}

async function withAppServer(run) {
  if (process.env.QA_BASE_URL) {
    process.stdout.write('[qa:all] QA_BASE_URL set, skipping managed dev server\\n');
    return run();
  }
  const startScript = pickAppServerScript();
  if (!startScript) {
    record('app-server', 'skipped', { reason: 'no dev/start script' });
    return run();
  }
  const logPath = path.join(RESULTS_DIR, 'app-server.log');
  const log = createWriteStream(logPath, { flags: 'a' });
  process.stdout.write('[qa:all] starting app server (npm run ' + startScript + '), logs -> qa-results/app-server.log\\n');
  const child = spawn('npm', ['run', startScript], {
    stdio: ['ignore', 'pipe', 'pipe'],
    shell: process.platform === 'win32',
  });
  child.stdout.pipe(log);
  child.stderr.pipe(log);
  const cleanup = () => { try { child.kill('SIGTERM'); } catch {} };
  process.on('exit', cleanup);
  const healthy = await waitForHealth(baseUrl, healthTimeoutMs);
  if (!healthy) {
    record('app-server', 'failed', { reason: 'health probe timed out at ' + baseUrl + ' — see qa-results/app-server.log' });
    cleanup();
    return false;
  }
  try {
    return await run();
  } finally {
    cleanup();
  }
}

