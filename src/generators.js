function hasScript(pkg, name) {
  return Boolean(pkg?.scripts?.[name]);
}

function addScript(scripts, name, command) {
  if (!scripts[name]) scripts[name] = command;
}

// Upgrade an agent-managed script: if the existing value is one of the known
// prior versions (or missing), replace with the new command. Preserves any
// user-customized command (where the existing value doesn't match a known
// prior version we shipped).
function upgradeScript(scripts, name, newCmd, knownPriorCmds = []) {
  if (!scripts[name] || knownPriorCmds.includes(scripts[name])) {
    scripts[name] = newCmd;
  }
}

function addDevDependency(devDependencies, name, version) {
  if (!devDependencies[name]) devDependencies[name] = version;
}

export function generateAssets(scan, plan, options = {}) {
  const pkg = scan.packageJson ? structuredClone(scan.packageJson) : { scripts: {} };
  pkg.scripts = pkg.scripts || {};
  pkg.devDependencies = pkg.devDependencies || {};

  const enabled = new Set(plan.enabledTools || []);
  const enrichment = options.enrichment instanceof Map ? options.enrichment : new Map();
  const journeysOverride = Array.isArray(options.journeys) ? options.journeys : null;
  const files = [];
  const deps = pkg.devDependencies;
  const qaOrder = plan.recommendedOrder.filter((script) => script !== "qa:all" && script !== "qa:report");

  addScript(pkg.scripts, "qa:prepare", "node -e \"require('fs').mkdirSync('qa-results',{recursive:true})\"");

  if (enabled.has("playwright")) {
    const journeys = journeysOverride || discoverUserJourneys(scan.files);
    upgradeScript(pkg.scripts, "qa:smoke",
      "PLAYWRIGHT_JSON_OUTPUT_FILE=playwright-report/smoke.json playwright test tests/smoke",
      ["playwright test tests/smoke"]);
    upgradeScript(pkg.scripts, "qa:e2e",
      "PLAYWRIGHT_JSON_OUTPUT_FILE=playwright-report/e2e.json playwright test tests/e2e/critical-journey.spec.ts",
      ["playwright test tests/e2e"]);
    upgradeScript(pkg.scripts, "qa:journeys",
      "PLAYWRIGHT_JSON_OUTPUT_FILE=playwright-report/journeys.json playwright test tests/e2e/user-journeys.spec.ts",
      ["playwright test tests/e2e/user-journeys.spec.ts"]);
    addDevDependency(deps, "@playwright/test", "^1.56.1");
    if (!scan.facts.hasPlaywrightConfig) files.push({ path: "playwright.config.ts", content: PLAYWRIGHT_CONFIG });
    files.push({ path: "tests/smoke/qa-smoke.spec.ts", content: PLAYWRIGHT_SMOKE });
    files.push({ path: "tests/e2e/critical-journey.spec.ts", content: PLAYWRIGHT_E2E });
    files.push({ path: "tests/e2e/user-journeys.spec.ts", content: createPlaywrightJourneySpec(journeys, enrichment) });
  }

  if (enabled.has("vitest")) {
    const newQaUnit = "npm run qa:prepare && vitest run tests/unit --coverage --reporter=json --outputFile=qa-results/vitest.json";
    const knownPriorQaUnit = "npm run qa:prepare && vitest run --coverage --reporter=json --outputFile=qa-results/vitest.json";
    if (!pkg.scripts["qa:unit"] || pkg.scripts["qa:unit"] === knownPriorQaUnit) {
      pkg.scripts["qa:unit"] = newQaUnit;
    }
    addDevDependency(deps, "vitest", "^4.1.5");
    addDevDependency(deps, "@vitest/coverage-v8", "^4.1.5");
    files.push({ path: "tests/unit/qa-baseline.test.js", content: VITEST });
    files.push({ path: "tests/unit/qa-generated-regression.test.js", content: createGeneratedUnitTests(discoverUnitTestTargets(scan)) });
  }

  if (enabled.has("sonarqube")) {
    addScript(pkg.scripts, "qa:quality", "echo \\\"Quality checks are now handled by Trivy!\\\"");
  }

  if (enabled.has("postman") || enabled.has("k6")) {
    const endpoints = discoverApiEndpoints(scan.files, { repoRoot: scan.root });
    if (enabled.has("postman")) {
      addScript(pkg.scripts, "qa:api", "npm run qa:prepare && newman run postman/qa-collection.json -e postman/qa-env.json --reporters cli,json --reporter-json-export qa-results/newman.json");
      addDevDependency(deps, "newman", "^6.2.1");
      files.push({ path: "postman/qa-collection.json", content: createPostmanCollection(endpoints) });
      files.push({ path: "postman/qa-env.json", content: createPostmanEnv(endpoints) });
    }
    if (enabled.has("k6")) {
      addScript(pkg.scripts, "qa:perf", "npm run qa:prepare && k6 run --summary-export qa-results/k6-summary.json tests/performance/load.js");
      files.push({ path: "tests/performance/load.js", content: createK6Script(endpoints) });
    }
  }

  if (enabled.has("axe") && enabled.has("playwright")) {
    const journeys = journeysOverride || discoverUserJourneys(scan.files);
    upgradeScript(pkg.scripts, "qa:a11y",
      "PLAYWRIGHT_JSON_OUTPUT_FILE=playwright-report/a11y.json playwright test tests/a11y/qa-a11y.spec.ts",
      ["playwright test tests/a11y/qa-a11y.spec.ts"]);
    addDevDependency(deps, "@axe-core/playwright", "^4.10.1");
    addDevDependency(deps, "axe-core", "^4.10.2");
    files.push({ path: "tests/a11y/qa-a11y.spec.ts", content: createAxeSpec(journeys) });
  }

  if (enabled.has("visual") && enabled.has("playwright")) {
    const journeys = journeysOverride || discoverUserJourneys(scan.files);
    upgradeScript(pkg.scripts, "qa:visual",
      "PLAYWRIGHT_JSON_OUTPUT_FILE=playwright-report/visual.json playwright test tests/visual/qa-visual.spec.ts",
      ["playwright test tests/visual/qa-visual.spec.ts"]);
    upgradeScript(pkg.scripts, "qa:visual:update",
      "PLAYWRIGHT_JSON_OUTPUT_FILE=playwright-report/visual.json playwright test tests/visual/qa-visual.spec.ts --update-snapshots",
      ["playwright test tests/visual/qa-visual.spec.ts --update-snapshots"]);
    files.push({ path: "tests/visual/qa-visual.spec.ts", content: createVisualSpec(journeys) });
  }

  if (enabled.has("gitleaks")) {
    addScript(pkg.scripts, "qa:secrets", "npm run qa:prepare && gitleaks detect --report-format json --report-path qa-results/gitleaks.json --no-git --no-banner --exit-code 0");
    files.push({ path: ".gitleaks.toml", content: GITLEAKS_CONFIG });
  }

  if (enabled.has("semgrep")) {
    addScript(pkg.scripts, "qa:sast", "npm run qa:prepare && semgrep --config auto --json --output qa-results/semgrep.json --quiet .");
  }

  if (enabled.has("trivy")) {
    const newQaSec = "npm run qa:prepare && trivy fs --format json --output qa-results/trivy.json --quiet .";
    pkg.scripts["qa:security"] = newQaSec;
    files.push({ path: ".trivyignore", content: createTrivyIgnore(scan) });
  }

  addScript(pkg.scripts, "qa:report", "node scripts/qa-report.mjs");
  if (qaOrder.some((script) => pkg.scripts[script])) addScript(pkg.scripts, "qa:all", "node scripts/qa-run-all.mjs");

  files.push({ path: "scripts/qa-run-all.mjs", content: createQaRunAll(qaOrder) });
  files.push({ path: "scripts/qa-report.mjs", content: QA_REPORT });

  return {
    packageJson: `${JSON.stringify(pkg, null, 2)}\n`,
    files,
  };
}
